package io.requery.android.sqlitex;

import android.annotation.SuppressLint;
import android.database.Cursor;
import android.database.sqlite.SQLiteException;
import io.requery.android.database.sqlite.SQLiteStatement;
import io.requery.android.sqlite.BaseStatement;
import io.requery.android.sqlite.CursorResultSet;
import io.requery.android.sqlite.SingleResultSet;

import java.sql.ResultSet;
import java.sql.SQLException;

class SqlitexStatement extends BaseStatement {

    protected final SqlitexConnection connection;

    SqlitexStatement(SqlitexConnection connection) {
        super(connection);
        this.connection = connection;
    }

    @Override
    public boolean execute(String sql, int autoGeneratedKeys) throws SQLException {
        SQLiteStatement statement = null;
        try {
            statement = connection.getDatabase().compileStatement(sql);
            if (autoGeneratedKeys == RETURN_GENERATED_KEYS) {
                long rowId = statement.executeInsert();
                insertResult = new SingleResultSet(this, rowId);
                return true;
            } else {
                statement.execute();
            }
        } catch (SQLiteException e) {
            SqlitexConnection.throwSQLException(e);
        } finally {
            if (statement != null) {
                statement.close();
            }
        }
        return false;
    }

    @Override
    public ResultSet executeQuery(String sql) throws SQLException {
        try {
            @SuppressLint("Recycle") // released with the queryResult
            Cursor cursor = connection.getDatabase().rawQuery(sql, null);
            return queryResult = new CursorResultSet(this, cursor, true);
        } catch (SQLiteException e) {
            SqlitexConnection.throwSQLException(e);
        }
        return null;
    }

    @Override
    public int executeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
        SQLiteStatement statement = null;
        try {
            statement = connection.getDatabase().compileStatement(sql);
            if (autoGeneratedKeys == RETURN_GENERATED_KEYS) {
                long rowId = statement.executeInsert();
                insertResult = new SingleResultSet(this, rowId);
                updateCount = 1;
            } else {
                updateCount = statement.executeUpdateDelete();
            }
        } catch (SQLiteException e) {
            SqlitexConnection.throwSQLException(e);
        } finally {
            if (statement != null) {
                statement.close();
            }
        }
        return updateCount;
    }
}
